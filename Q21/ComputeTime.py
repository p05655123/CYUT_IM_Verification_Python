# 請你寫一個程式，根據現在的時間及鬧鈴的時間算出間隔幾分鐘。輸入說
# 明 ：每筆測試資料含有四個整數 h1、m1、h2 及 m2，h1:m1代表現在的時
# 與分，h2:m2則代表鬧鈴所設的時間 (時與分)，(0≤h1≤23, 0≤m1≤59, 0≤h2≤23, 
# 0≤m2≤59)。輸出說明 ：對於每筆測試資料，你的程式要把分鐘數單獨輸出於一行。
# (請上傳ComputeTime.py)

h1 = int(input("h1 :"))
m1 = int(input("m1 :"))
h2 = int(input("h2 :"))
m2 = int(input("m2 :"))

if h1 == 0:
    h1 = 24

if h2 == 0: 
    h2 = 24

s1 = h1 * 60 + m1
s2 = h2 * 60 + m2

if h2 == h1 and m2 < m1:
    print((s2-s1)+(24*60))
else:
    print(s2-s1)